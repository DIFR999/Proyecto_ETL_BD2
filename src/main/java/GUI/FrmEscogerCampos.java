    private void btnAgregarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCActionPerformed
        
        try {
            // Obtener el elemento seleccionado del JComboBox
            String selectedItem = (String) cmbCamposTablaOrigen.getSelectedItem();
            int indexCampo = cmbCamposTablaOrigen.getSelectedIndex();
            
            
                      // Construir el formato del elemento que se agregar√° al listModel

              String elemento = MenuPrincipal.campos.get(indexCampo).getColumnName() + " " +
               MenuPrincipal.campos.get(indexCampo).getDataType() + " " +
                MenuPrincipal.campos.get(indexCampo).getMaxLength();
            boolean existeLista = false;
            for (int i = 0; i < listModel.getSize(); i++) {
                if (listModel.getElementAt(i).equals(elemento)) {
                    existeLista = true;
                    break;
                }
            }

            if (selectedItem != null) {
                if(!existeLista){
                    /*listModel.addElement(FormularioETL.camposSelectOrigen.get(indexCampo).getColumnName()+" "+FormularioETL.camposSelectOrigen.get(indexCampo).getDataType()
                             +" "+FormularioETL.camposSelectOrigen.get(indexCampo).getMaxLength());*/
                    
              
                    listModel.addElement(elemento); // Agregar al modelo de la lista

                     jlsCamposSeleccionados.setModel(listModel);
                     FormularioETL.camposSelectOrigen.add(MenuPrincipal.campos.get(indexCampo));
                     btnGuardarCampos.setEnabled(true);

                }else{
                    JOptionPane.showMessageDialog(this, "Ya selecciono ese campo");
  
                }      
                
               

           
                
            }   
            
            
        }catch(Exception e) {
                
           JOptionPane.showMessageDialog(this, "Succedio un error inesperado"+ e.getMessage());

        }
       
            
            
    }//GEN-LAST:event_btnAgregarCActionPerformed

